package com.example.unaig.satd_1;

public class PeticionRecomendacion {
    private String TenistaUno;
    private String TenistaDos;
    private String Superficie;

    public String getTenistaUno() {
        return TenistaUno;
    }

    public void setTenistaUno(String tenistaUno) {
        TenistaUno = tenistaUno;
    }

    public String getTenistaDos() {
        return TenistaDos;
    }

    public void setTenistaDos(String tenistaDos) {
        TenistaDos = tenistaDos;
    }

    public String getSuperficie() {
        return Superficie;
    }

    public void setSuperficie(String superficie) {
        Superficie = superficie;
    }
}
